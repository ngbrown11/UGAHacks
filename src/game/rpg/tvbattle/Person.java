package game.rpg.tvbattle;
// MULTIVERSE BATTLEGROUND
// Where universes collide

public class Person {
	
	private int health = 100;
	private int speed;
	private int punch;
	private int kick;
	public int getSpeed() {
		return speed;
	}
	public void setSpeed(int speed) {
		this.speed = speed;
	}
	public int getPunch() {
		return punch;
	}
	public void setPunch(int punch) {
		this.punch = punch;
	}
	public int getKick() {
		return kick;
	}
	public void setKick(int kick) {
		this.kick = kick;
	}
	
	
}

